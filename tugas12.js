//mendefinisikan variable array nama-nama Buah
var namaBuah = ["Pisang", "Jeruk", "Apel", "Mangga"];
//menampilkan data nama-nama buah
console.log(namaBuah);

//menghapus data awal dengan shift 
namaBuah.shift();
